/**
 * Mapping from ISO 3166-1 alpha-2 codes to country flag SVG paths.
 * Flags sourced from flag-icons (MIT license): https://github.com/lipis/flag-icons
 * Covers 197 countries: 193 UN members + Vatican City + Palestine + Taiwan + Kosovo.
 */

export type FlagRatio = "4x3" | "1x1";

/** Returns the public URL path for a country flag SVG. */
export function getFlagUrl(isoA2: string, ratio: FlagRatio = "4x3"): string {
  return `/flags/${ratio}/${isoA2.toLowerCase()}.svg`;
}

/**
 * Resolves a flag URL from GeoJSON properties, handling edge cases where
 * ISO_A2 is missing or non-standard in Natural Earth data.
 */
export function getFlagUrlFromGeoJson(
  isoA2: string,
  isoA3: string,
  name: string,
  ratio: FlagRatio = "4x3"
): string {
  const resolved = resolveIsoA2(isoA2, isoA3, name);
  if (!resolved) return "";
  return getFlagUrl(resolved, ratio);
}

/**
 * Resolves a valid ISO alpha-2 code from GeoJSON properties.
 * Handles Natural Earth edge cases where ISO_A2 is -99 or non-standard.
 */
function resolveIsoA2(
  isoA2: string,
  isoA3: string,
  name: string
): string | null {
  // Standard case
  if (isoA2 && isoA2 !== "-99" && !isoA2.includes("-")) {
    return isoA2.toLowerCase();
  }

  // Taiwan is "CN-TW" in Natural Earth
  if (isoA2 === "CN-TW" || isoA3 === "TWN") return "tw";

  // Natural Earth omits ISO codes for these
  const NAME_OVERRIDES: Record<string, string> = {
    Norway: "no",
    France: "fr",
    Kosovo: "xk",
    "N. Cyprus": "cy", // Uses Cyprus flag as fallback
    Somaliland: "so", // Uses Somalia flag as fallback
  };

  return NAME_OVERRIDES[name] ?? null;
}

