const translationCache = new Map<Locale, Translations>()
translationCache.set('en', en)

export async function loadTranslations(locale: Locale): Promise<Translations> {
  const cached = translationCache.get(locale)
  if (cached) return cached

  let translations: Translations
  switch (locale) {
    case 'fr':
      translations = (await import('./lang/fr')).default
      break
    case 'de':
      translations = (await import('./lang/de')).default
      break
    case 'nl':
      translations = (await import('./lang/nl')).default
      break
    case 'es':
      translations = (await import('./lang/es')).default
      break
    case 'zh':
      translations = (await import('./lang/zh')).default
      break
    default:
      return en
  }

  translationCache.set(locale, translations)
  return translations
}

