const SRC_4x3 = path.join(__dirname, "..", "node_modules", "flag-icons", "flags", "4x3");
const SRC_1x1 = path.join(__dirname, "..", "node_modules", "flag-icons", "flags", "1x1");
const DEST_4x3 = path.join(__dirname, "..", "public", "flags", "4x3");
const DEST_1x1 = path.join(__dirname, "..", "public", "flags", "1x1");

fs.mkdirSync(DEST_4x3, { recursive: true });
fs.mkdirSync(DEST_1x1, { recursive: true });

let copied = 0;
const missing = [];

for (const country of COUNTRIES) {
  const filename = `${country.code}.svg`;
  const src4x3 = path.join(SRC_4x3, filename);
  const src1x1 = path.join(SRC_1x1, filename);

  if (fs.existsSync(src4x3) && fs.existsSync(src1x1)) {
    fs.copyFileSync(src4x3, path.join(DEST_4x3, filename));
    fs.copyFileSync(src1x1, path.join(DEST_1x1, filename));
    copied++;
  } else {
    missing.push(country);
  }
}

console.log(`Copied ${copied}/${COUNTRIES.length} flags (4x3 + 1x1)`);
if (missing.length > 0) {
  console.log("Missing flags:");
  missing.forEach((c) => console.log(`  ${c.code} — ${c.name}`));
}
